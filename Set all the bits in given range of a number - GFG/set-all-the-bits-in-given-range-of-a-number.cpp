class Solution {
  public:
    int setAllRangeBits(int N , int L , int R) {
        int i =L ;
        int sum ;
        while(i<=R) {
        sum  = sum + pow(2,i-1) ;
        i++ ;
            
        }
        return sum|N ;
        // code here
       
    }
};

